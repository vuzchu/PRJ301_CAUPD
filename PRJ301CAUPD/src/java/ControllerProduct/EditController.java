    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        ProductsDBContext db = new ProductsDBContext();

        int product__id = Integer.parseInt(request.getParameter("product__id"));
        Product detail = db.getProductByProductID(product__id);
        request.setAttribute("detail", detail);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        
        Product p = new Product();

        p.setProduct_name(request.getParameter("product_name"));
        p.setPrice(Float.parseFloat(request.getParameter("price")));
        p.setImage(request.getParameter("image"));
        p.setDescription(request.getParameter("description"));
        p.setCid(Integer.parseInt(request.getParameter("category")));

        HttpSession session = request.getSession();
        Account account = (Account) session.getAttribute("account");
        int sell_id = account.getUid();

        ProductsDBContext pdb = new ProductsDBContext();

        pdb.edit(p);

        response.sendRedirect("manager");
    }

